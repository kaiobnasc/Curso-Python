c = float(input('Digite a temperatura em °C: ')) # Print que pede ao usuário uma temperatura em graus celcius
f = ((9*c)/5) + 32 # Variável f recebe o cálculo da conversão para Farenheit

print("A temperatura de {}°C corresponde a {}°F".format(c, f)) # Print que informa a temperatura em Celsius e sua conversão para Farenheit
n1 = int(input("Digite o número: ")) # Print que pede ao usuário um número para ser digitado
antecessor = n1 - 1 # Cálculo para indicar o antecessor
sucessor = n1 + 1 # Cálculo para indicar o sucessor

print("O número {} tem como sucessor {} e como antecessor {}.".format(n1, sucessor, antecessor)) # Print que retorna o numero, seu sucessor e seu antecessor


